void CPlanarMirrorShader::CreateShaderVariables(ID3D12Device* pd3dDevice, ID3D12GraphicsCommandList* pd3dCommandList)
{
	UINT ncbElementBytes = ((sizeof(VS_CB_REFLECTION_MATRIX) + 255) & ~255); //256의 배수
	m_pd3dcbReflection = ::CreateBufferResource(pd3dDevice, pd3dCommandList, NULL, ncbElementBytes, D3D12_HEAP_TYPE_UPLOAD, D3D12_RESOURCE_STATE_VERTEX_AND_CONSTANT_BUFFER, NULL);

	m_pd3dcbReflection->Map(0, NULL, (void**)&m_pcbMappedReflection);
}

